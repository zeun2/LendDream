package lenddream;

public class LendProduct {
	private int prod_num;
	private String price;
    private String id;
    private String prod_name;
	private String category;
	private String description;
	private String from_d;
	private String to_d;
    private String img;
    private int view;
    private int lend_count;
    
	public int getProd_num() {
		return prod_num;
	}
	public void setProd_num(int prod_num) {
		this.prod_num = prod_num;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getFrom_d() {
		return from_d;
	}
	public void setFrom_d(String from_d) {
		this.from_d = from_d;
	}
	public String getTo_d() {
		return to_d;
	}
	public void setTo_d(String to_d) {
		this.to_d = to_d;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getView() {
		return view;
	}
	public void setView(int view) {
		this.view = view;
	}
	public int getLend_count() {
		return lend_count;
	}
	public void setLend_count(int lend_count) {
		this.lend_count = lend_count;
	}
    

}
